/*
  PRETTIER + ESLINT
    Ferramentas que auxiliam no desenvolvimento de projetos.

    - Eslint auxilia a encontrar erros na sintaxe do codigo e avisa.
    - Prettier ajuda com a identação do codigo, deixando mais organizado.
        - Também ajuda na padronização de codigo.
    - Tanto Eslint quanto Prettier são dependencias de desenvolvimento.

    instalando as ferramentas:

    yarn add eslint -D  
    yarn eslint --init
    
    yarn add prettier eslint-config-prettier eslint-plugin-prettier -D 
    yarn add eslint-plugin-react-hooks -D 
    yarn add eslint-plugin-import-helpers -D
    

    depois de instaladas as ferramentas, podemos configurar de acordo com nossas necessidades
    dentro do arquivo eslintrc.json




*/
